#include "libft.h"

void	*ft_memset(void *buf, int ch, size_t n)
{
	int		a;
	char	*h;

	h = buf;
	a = 0;
	while (0 < n)
	{
		h[a] = ch;
		n--;
		a++;
	}
	return (buf);
}
